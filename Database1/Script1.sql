
CREATE TABLE Employees (
ID INT NOT NULL Primary key, 
Name CHAR(25) NOT NULL, 
Surname CHAR(25) NOT NULL, 
BirthDate DATE NOT NULL, 
PhoneNumber CHAR(25) NOT NULL, 
HomeAddress CHAR(25) NOT NULL,
IDCompany INT NOT NULL);

